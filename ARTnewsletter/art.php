									<img width="100%"  class="img-fluid" src="media/blacksmith_cover.jpg" alt="gameplay coming soon"><p><i>Gameplay video af vikingesmeden er på vej!</i></p>
									<br>
									<p>Virtual Reality-oplevelsen tager dig med tilbage til vikingetiden, hvor du som smedens l&aelig;ring, har mulighed for at smede klingen på et sv&aeling;rd. Din mester, der sidder ved din side, fort&aelig;ller dig hvad du skal g&oslash;re og r&aring;ber af dig hvis du gør det forkert. Når du hamrer på det r&oslash;dgl&oslash;dende metal på ambolten når du tr&aeling;kker i bl&aelig;seb&aelig;lgen g&oslash;r det oplevelsen b&aring;de autentisk og informativ.</p>
									<br>
									<p>Spillet er vores mest popul&aeling;re og velkendte VR-oplevelse og er et godt eksempel på hvordan virtual reality kan være en gamechanger n&aring;r det handler om historisk formidling og fort&aeling;lling. Attraktionen er en mindeværdig oplevelse, skaber engagement og giver en enestående brugertilfredshed for museumsg&aelig;sterne. </p>
