									<p>Inspireret af et igangværende samarbejde mellem Malstr&oslash;m og Ribe Vikingecenter mødtes vi, i vinteren 2016, med Bjarne Clement (Museums inspekt&oslash;r, Ribe Vikingecenter) og Simon Lykke (Partner, Malstr&oslash;m) for at diskutere mulighederne indenfor virtual reality og gamification. </p>
